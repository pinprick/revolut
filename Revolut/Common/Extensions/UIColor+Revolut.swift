import Foundation
import UIKit

extension UIColor {
    static var tintBlueColor: UIColor {
        return UIColor(red: 0.0, green: 122.0 / 255.0, blue: 1.0, alpha: 1.0)
    }
    
    static var tintGrayColor: UIColor {
        return UIColor.gray
    }
}
